class Personality {
  final String name;
  final int age;
  final int height;
  final int weight;
  final String story;
  final String eyesColor;
  final String hairColor;
  final String skinColor;

  Personality({
    required this.eyesColor,
    required this.hairColor,
    required this.skinColor,
    required this.name,
    required this.age,
    required this.height,
    required this.weight,
    required this.story,
  });
}
